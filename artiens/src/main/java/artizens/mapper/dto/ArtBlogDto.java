package artizens.mapper.dto;

public class ArtBlogDto {
	private String creatorId;
	private String nickname;
	private String otheruri;
	private String storefilename;
	private String uploadfilename;
	private String title;
	private String recentday;
	private String popular;
	
	
	public String getCreatorId() {
		return creatorId;
	}
	public void setCreatorId(String creatorId) {
		this.creatorId = creatorId;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public String getOtheruri() {
		return otheruri;
	}
	public void setOtheruri(String otheruri) {
		this.otheruri = otheruri;
	}
	public String getStorefilename() {
		return storefilename;
	}
	public void setStorefilename(String storefilename) {
		this.storefilename = storefilename;
	}
	public String getUploadfilename() {
		return uploadfilename;
	}
	public void setUploadfilename(String uploadfilename) {
		this.uploadfilename = uploadfilename;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getRecentday() {
		return recentday;
	}
	public void setRecentday(String recentday) {
		this.recentday = recentday;
	}
	public String getPopular() {
		return popular;
	}
	public void setPopular(String popular) {
		this.popular = popular;
	}
	
	
}
