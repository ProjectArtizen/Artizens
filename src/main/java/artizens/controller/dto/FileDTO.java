package artizens.controller.dto;

public class FileDTO {
	
	String Filename;
	String Filesize;
	
	public String getFilename() {
		return Filename;
	}
	public void setFilename(String filename) {
		Filename = filename;
	}
	public String getFilesize() {
		return Filesize;
	}
	public void setFilesize(String filesize) {
		Filesize = filesize;
	}
	
	
}
